def make_repeated_tokens(batch=10, seq_len=50, vocab_size=1000):
    half = seq_len // 2
    tokens = torch.randint(0, vocab_size, (batch, half))
    tokens = torch.cat([tokens, tokens], dim=1) # We are essetntially repeating a sequence of tokens
    return tokens

